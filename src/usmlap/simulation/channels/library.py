class CentripetalForce(
    Channel[Data], name="Centripetal Force", unit=Unit.NEWTON
):
    """Centripetal force acting on the vehicle."""

    def _channel_fcn(self, solution: Solution) -> Data:
        return [
            node.calculated_vehicle_state.centripetal_force for node in solution
        ]


