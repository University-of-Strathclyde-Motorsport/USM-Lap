def lap_average(channel: fcn.ChannelFcn) -> fcn.ChannelFcn:
    def inner(solution: Solution) -> list[float]:
        lap_solutions = solution.get_lap_solutions()
        return [fcn.average(channel)(s) for s in lap_solutions]

    return inner


def lap_max(channel: fcn.ChannelFcn) -> fcn.ChannelFcn:
    def inner(solution: Solution) -> list[float]:
        lap_solutions = solution.get_lap_solutions()
        return [fcn.maximum(channel)(s) for s in lap_solutions]

    return inner


def lap_total(channel: fcn.ChannelFcn) -> fcn.ChannelFcn:
    def inner(solution: Solution) -> list[float]:
        lap_solutions = solution.get_lap_solutions()
        return [fcn.total(channel)(s) for s in lap_solutions]

    return inner


class LapTime(Channel[Data], name="Lap Time", unit=Unit.SECOND):
    """Lap time of the vehicle."""

    def _channel_fcn(self, solution: Solution) -> Data:
        return lap_total(SegmentTime())(solution)


